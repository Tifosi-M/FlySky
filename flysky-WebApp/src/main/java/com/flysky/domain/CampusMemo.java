package com.flysky.domain;

import java.util.Date;

public class CampusMemo {
	private String campusName;
	private String campusBy;
	private String campusTime;
	private String campusState;
	private String campusAddress;
	private String campusContent;
	public String getCampusName() {
		return campusName;
	}
	public void setCampusName(String campusName) {
		this.campusName = campusName;
	}
	public String getCampusBy() {
		return campusBy;
	}
	public void setCampusBy(String campusBy) {
		this.campusBy = campusBy;
	}
	public String getCampusTime() {
		return campusTime;
	}
	public void setCampusTime(String campusTime) {
		this.campusTime = campusTime;
	}
	public String getCampusState() {
		return campusState;
	}
	public void setCampusState(String campusState) {
		this.campusState = campusState;
	}
	public String getCampusAddress() {
		return campusAddress;
	}
	public void setCampusAddress(String campusAddress) {
		this.campusAddress = campusAddress;
	}
	public String getCampusContent() {
		return campusContent;
	}
	public void setCampusContent(String campusContent) {
		this.campusContent = campusContent;
	}
	
}
	
	
